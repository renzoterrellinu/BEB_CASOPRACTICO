        String sql = "INSERT INTO planes_inversion (tipo_plan, monto, rentabilidad, id_cliente) VALUES (?, ?, ?, ?)";

        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setString(1, jTextField2.getText());
        ps.setDouble(2, Double.parseDouble(jTextField3.getText()));
        ps.setDouble(3, Double.parseDouble(jTextField4.getText()));
        ps.setInt(4, Integer.parseInt(jTextField5.getText()));

