    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int fila = jTable1.getSelectedRow();

    if (fila >= 0) {
        jTextField1.setText(jTable1.getValueAt(fila, 0).toString()); // ID
        jTextField2.setText(jTable1.getValueAt(fila, 1).toString()); // Tipo
        jTextField3.setText(jTable1.getValueAt(fila, 2).toString()); // Monto
        jTextField4.setText(jTable1.getValueAt(fila, 3).toString()); // Rentabilidad
        jTextField5.setText(jTable1.getValueAt(fila, 4).toString()); // ID Cliente
    } else {
        JOptionPane.showMessageDialog(null, "Selecciona una fila");
    }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
        String sql = "INSERT INTO planes_inversion (tipo_plan, monto, rentabilidad, id_cliente) VALUES (?, ?, ?, ?)";

        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setString(1, jTextField2.getText());
        ps.setDouble(2, Double.parseDouble(jTextField3.getText()));
        ps.setDouble(3, Double.parseDouble(jTextField4.getText()));
        ps.setInt(4, Integer.parseInt(jTextField5.getText()));

        ps.executeUpdate();

        JOptionPane.showMessageDialog(null, "Plan agregado correctamente");

        listar();
        limpiar();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e.getMessage());
    }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        try {
        String sql = "UPDATE planes_inversion SET tipo_plan=?, monto=?, rentabilidad=?, id_cliente=? WHERE id_plan=?";

        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setString(1, jTextField2.getText());
        ps.setDouble(2, Double.parseDouble(jTextField3.getText()));
        ps.setDouble(3, Double.parseDouble(jTextField4.getText()));
        ps.setInt(4, Integer.parseInt(jTextField5.getText()));
        ps.setInt(5, Integer.parseInt(jTextField1.getText())); // ID

        int res = ps.executeUpdate();

        if (res > 0) {
            JOptionPane.showMessageDialog(null, "Plan actualizado");
        } else {
            JOptionPane.showMessageDialog(null, "No se encontró el plan");
        }

        listar();
        limpiar();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e.getMessage());
    }
    }//GEN-LAST:event_jButton3ActionPerformed

