    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int fila = jTable1.getSelectedRow();
        if (fila >= 0) {
        jTextField1.setText(jTable1.getValueAt(fila, 0).toString());
        jTextField2.setText(jTable1.getValueAt(fila, 1).toString());
        jTextField3.setText(jTable1.getValueAt(fila, 2).toString());
        jTextField4.setText(jTable1.getValueAt(fila, 3).toString());
    }
        else {
        JOptionPane.showMessageDialog(null, "Selecciona una fila");
    }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
          try {
        String sql = "INSERT INTO cuentas_ahorro(saldo, fecha_apertura, id_cliente) VALUES (?, ?, ?)";

        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setDouble(1, Double.parseDouble(jTextField2.getText()));
        ps.setString(2, jTextField3.getText());
        ps.setInt(3, Integer.parseInt(jTextField4.getText()));

        ps.executeUpdate();

        JOptionPane.showMessageDialog(null, "Cuenta agregada");

        listar();
        limpiar();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e.getMessage());
    }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
         try {
        String sql = "UPDATE cuentas_ahorro SET saldo=?, fecha_apertura=?, id_cliente=? WHERE id_cuenta=?";

        PreparedStatement ps = conn.prepareStatement(sql);
        ps.setDouble(1, Double.parseDouble(jTextField2.getText()));
        ps.setString(2, jTextField3.getText());
        ps.setInt(3, Integer.parseInt(jTextField4.getText()));
        ps.setInt(4, Integer.parseInt(jTextField1.getText()));

        int resultado = ps.executeUpdate();

        if (resultado > 0) {
            JOptionPane.showMessageDialog(null, "Cuenta actualizada");
        } else {
            JOptionPane.showMessageDialog(null, "No se encontró la cuenta");
        }

        listar();
        limpiar();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e.getMessage());
    }
    }//GEN-LAST:event_jButton3ActionPerformed

